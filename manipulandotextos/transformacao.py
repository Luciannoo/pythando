# transformação de frases

frase0 = "Manipulando Texto em Python"
# aqui, ele substiui a frase indicada por outra
print(frase0.replace("Texto", "String"))

print("===============================")

frase1 = "Manipulando Texto em Python"
# deixa todas as frases que estão minusculas em mausculas
print(frase1.upper())

print("===============================")

frase2 = "Manipulando Texto em Python"
# deixa todas as frases que estão em maiuscula em minusculas
print(frase2.lower())

print("===============================")

frase3 = "Manipulando Texto em Python"
# deixa todas as frases em minusculas e só a primeira letra maíscula
print(frase3.capitalize())

print("===============================")

frase4 = "   Manipulando Texto em Python    "
# remove todos os espaçoes em branco que são inuteis do ínicio e do final da frase
print(frase4.strip())

print("===============================\n")